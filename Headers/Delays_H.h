//--------------------------------------------------------------------------
// nop	 
//--------------------------------------------------------------------------
//
// Return Value : None
// Parameters 	: None
// Discription	: delay one empty itteraction
//
//-------------------------------------------------------------------------
void nop(void){ } // delay 5usec

//--------------------------------------------------------------------------
// delay_us	 
//--------------------------------------------------------------------------
//
// Return Value : None
// Parameters 	: number of mikroseconds
// Discription	: delay during several mikroseconds
//
//-------------------------------------------------------------------------
void delay_us(u16 us)		  //	usec(10)=200usec ;  usec(1)=20usec ;
{ 
  while(us--) nop();
}

//--------------------------------------------------------------------------
// delay_ms
//--------------------------------------------------------------------------
//
// Return Value : None
// Parameters	: number of miliseconds
// Discription	: delay during several miliseconds
//
//-------------------------------------------------------------------------
void delay_ms(u16 ms)
{
	u16 n;
	u16 i;

	for (n=0; n<ms; n++)
	{
		for (i=0; i<100; i++); 
	} 
} 


